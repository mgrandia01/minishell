#include "../includes/libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*nodo1;

	nodo1 = (t_list *)malloc(sizeof(t_list));
	if (!nodo1)
		return (NULL);
	nodo1->content = content;
	nodo1->next = NULL;
	return (nodo1);
}
